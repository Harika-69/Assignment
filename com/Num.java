class Num
{
 public static void main(String[]args)
{ 
  int n=100;
  System.out.println("even numbers from 1 to "+n+"are: ");
  for(int i=1;i<=n;i++)
  {
   if(i % 2 ==0)
   {
     System.out.println(i+" ");
    }
   }
  System.out.println("odd numbers from 1 to "+n+"are: ");
  for(int j=1;j<=n;j++)
  {
   if(j % 2 !=0)
   {
    System.out.println(j+" ");
  }
 }
}
}


 
